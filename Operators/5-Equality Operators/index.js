// Strict Equality Operators (Type + Value) both check

console.log(1 === 1); // It will return true   
console.log('1' === 1); // It will return false because the type is different

// Lose Equality Operators only check (Value) 
console.log(1 == 1); // It will return true
console.log('1' == 1); // It will return true
console.log(true == 1); // It will return true

