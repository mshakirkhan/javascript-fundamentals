let x = 12;
let y = 6;

console.log(x + y); // Addition Operator
console.log(x - y); // Subtraction Operator
console.log(x * y); // Multiplication Operator
console.log(x / y); // Division Operator
console.log(x % y); // Reminder Operator
console.log(x ** y); // Exponentiation Operator


// Increment And Decrement Operator (++, --)

// Example of pre-increment operator
console.log(++x); // And will be 13

// Example of post-increment operator
console.log(x++); // And ans will be 12 because we are using post increment it is adding later.
console.log(x); // Now the ans will be 13 because above we used post increment.

// Example of pre-decrement operator
console.log(--x); // And will be 12

// Example of post-decrement operator
console.log(x--); // And ans will be 12 because we are using post decrement it is decrement later.
console.log(x); // Now the ans will be 11 because above we used post decrement.
