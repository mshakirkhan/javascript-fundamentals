// Combining Array Example
let arrayOne = [1,2,3,4,5];
let arrayTwo = [6,7,8,9,10];

let combinedArray = arrayOne.concat(arrayTwo);
console.log(combinedArray);

// Slicing Array Example
// Start and End if we passed only one parameter that will be starting point
let sliceArray = combinedArray.slice(2,4); 
console.log(sliceArray);