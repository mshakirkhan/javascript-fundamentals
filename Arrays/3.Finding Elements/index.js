// Method 01 Example:
const number = [1,2,3,4,5,1,6,7];

// Index Of will return index number when value is exist otherwise it will return -1.
console.log(number.indexOf(1)) 

// lastIndexOf will return the last index no when record is found otherwise it will -1
console.log(number.lastIndexOf(1)) 

// Method 02 Example:

// Includes will return boolean, if record found it will return true
console.log(number.includes(1)); 